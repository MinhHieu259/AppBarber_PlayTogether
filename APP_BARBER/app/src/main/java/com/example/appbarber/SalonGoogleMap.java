package com.codingwithmitch.googlemaps1412022.Model;

public class SalonGoogleMap {
    private int id;
    private String tenSalon;
    private String chuTiem;
    private String diaChi;
    private String hinhAnh;
    private double latitude;
    private double longitude;
    private String gioiThieu;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenSalon() {
        return tenSalon;
    }

    public void setTenSalon(String tenSalon) {
        this.tenSalon = tenSalon;
    }

    public String getChuTiem() {
        return chuTiem;
    }

    public void setChuTiem(String chuTiem) {
        this.chuTiem = chuTiem;
    }

    public String getDiaChi() {
        return diaChi;
    }

    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }

    public String getHinhAnh() {
        return hinhAnh;
    }

    public void setHinhAnh(String hinhAnh) {
        this.hinhAnh = hinhAnh;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public String getGioiThieu() {
        return gioiThieu;
    }

    public void setGioiThieu(String gioiThieu) {
        this.gioiThieu = gioiThieu;
    }
}
